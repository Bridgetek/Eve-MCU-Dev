/* Define a "safe" prescaler for QUADSPI. This is used during initialisation
 * and must be below 11 MHz. The fastest speed is usually in the order of
 * 120 to 240 MHz. So a prescalar of 64 will be safely within that limit.
 * The speed will be set to the full speed after initialisation. */
#define QSPI_CLK_DIV (64 - 1)


#if defined(QUADSPI_ENABLE)
#pragma message ("SPI Mode: Quad channel")
#if IS_EVE_API(1)
#error Quad SPI is not supported on EVE API 1 (FT80x)
#endif
#else
#pragma message ("SPI Mode: Single channel")
#endif

/* SPI handler declaration */
/* There is only one QUADSPI channel available. */
extern QSPI_HandleTypeDef hqspi;

/* Transfers are "chunked" to the EVE by the HAL.
 * This buffer is large enough to receive one chunk of
 * data and transmit it in one go. If it cannot be
 * sent in one go then the write address may not be
 * valid on subsequent packets.
 */
#define MCU_BUFFER_SIZE (HAL_MAX_CHUNK_SIZE)
static uint8_t *MCU_buffer;
static uint16_t MCU_bufferLen;

/* Forward declaration */
#if 0
static void Error_Handler(void);
#endif

/* Default QuadSPI off. */
static int ftIsQuad = 0;

int MCU_Init(void)
{
    MCU_buffer = malloc(MCU_BUFFER_SIZE);
    if (MCU_buffer == NULL)
    {
        DEBUG_ERROR("QSPI Setup malloc failed\n");
        return -1;
    }

    MCU_bufferLen = 0;

    /* Set the QUADSPI speed to a safe minimum. It must be
     * under 11 MHz but 1 MHz is very safe.
     * Use the "safe" prescaler to get a safe clock for SPI
     * during initialisation.
     */
    hqspi.Init.ClockPrescaler = QSPI_CLK_DIV;
    if (HAL_QSPI_Init(&hqspi) != HAL_OK)
    {
      Error_Handler();
      return -1;
    }

    return 0;
}

int MCU_Deinit(void)
{
    HAL_QSPI_DeInit(&hqspi);
    if (MCU_buffer)
    {
        free(MCU_buffer);
        MCU_buffer = NULL;
    }

    return 0;
}

int MCU_Setup(void)
{
    /* Increase SPI speed after initialisation is complete.
     * See the notes for MCU_SPI_TIMEOUT in the MCU.h file.
     * This will set the QUADSPI to maximum speed configured
     * in STM32CubeMX.
     * This can be a maximum of 60 MHz for BT820, or 25 MHz
     * on FT81x, BT88x, BT81x. */
    MX_QUADSPI_Init();

#if defined QUADSPI_ENABLE
#if IS_EVE_API(2,3,4,5)
    /* Select QSPI after initialisation complete. */
    HAL_SetSPIMode(2);
    ftIsQuad = 1;
#else // IS_EVE_API(2,3,4,5)
    ftIsQuad = 0;
#endif
#endif // QUADSPI_ENABLE

    return 0;
}

/* Send data in the write buffer and read in bytes from the QSPI. */
static HAL_StatusTypeDef MCU_multi_transfer(uint8_t *DataToRead, uint32_t len)
{
    QSPI_CommandTypeDef sCommand = {0};
    HAL_StatusTypeDef status;

    /* There will be only read OR write operations to perform. */
    /* If the write buffer length is greater than 4 bytes for BT82x or 
     * 3 bytes for previous devices then we are writing. */
    if ((MCU_bufferLen > 0) && (len == 0))
    {
    	/* Write the contents of the buffer to the EVE device.
    	 * The address is in big-endian format in the first 4 bytes for
    	 * EVE API 5 and the first 3 for previous generations.
    	 */
        sCommand.InstructionMode   = QSPI_INSTRUCTION_NONE;
        sCommand.AddressMode       = QSPI_ADDRESS_NONE;
        sCommand.AddressSize       = QSPI_ADDRESS_32_BITS;
        if (ftIsQuad == 0)
        {
            sCommand.DataMode          = QSPI_DATA_1_LINE;
        }
        else
        {
            sCommand.DataMode          = QSPI_DATA_4_LINES;
        }
		sCommand.NbData            = MCU_bufferLen;
        sCommand.DummyCycles       = 0;
        sCommand.DdrMode           = QSPI_DDR_MODE_DISABLE;
        sCommand.DdrHoldHalfCycle  = QSPI_DDR_HHC_ANALOG_DELAY;
        sCommand.SIOOMode          = QSPI_SIOO_INST_EVERY_CMD;

        status = HAL_QSPI_Command(&hqspi, &sCommand, HAL_MAX_DELAY);
        if (status != HAL_OK)
        {
            DEBUG_PRINTF("QSPI command sending error in QSPI_Transmit...\n");
            return status;
        }
        else
        {
			status = HAL_QSPI_Transmit(&hqspi, MCU_buffer, HAL_MAX_DELAY);
			if (status != HAL_OK)
			{
				DEBUG_PRINTF("QSPI Transmit error in QSPI_Transmit...\n");
				return status;
			}
		}
        MCU_bufferLen = 0;
    }
    else if ((MCU_bufferLen > 0) && (len > 0))
    {
    	/* Read from the EVE device to the buffer in the parameters.
    	 * The address is previously set in big-endian format in the first 4
    	 * bytes of the transmit buffer for all generations.
    	 */
        /* Reconstruct the big-endian address from the MCU_buffer array */
        uint32_t addr = (MCU_buffer[0] << 24) | (MCU_buffer[1] << 8) | (MCU_buffer[2] << 8) | (MCU_buffer[3] << 0);

        sCommand.InstructionMode   = QSPI_INSTRUCTION_NONE;
        sCommand.AddressSize       = QSPI_ADDRESS_32_BITS;
        sCommand.Address           = addr;

        if (ftIsQuad == 0)
        {
            sCommand.AddressMode       = QSPI_ADDRESS_1_LINE;
            sCommand.DataMode          = QSPI_DATA_1_LINE;
        }
        else
        {
            sCommand.AddressMode       = QSPI_ADDRESS_4_LINES;
            sCommand.DataMode          = QSPI_DATA_4_LINES;
        }
        sCommand.NbData            = len;
        sCommand.DummyCycles       = 0;
        sCommand.DdrMode           = QSPI_DDR_MODE_DISABLE;
        sCommand.DdrHoldHalfCycle  = QSPI_DDR_HHC_ANALOG_DELAY;
        sCommand.SIOOMode          = QSPI_SIOO_INST_EVERY_CMD;

        status = HAL_QSPI_Command(&hqspi, &sCommand, HAL_MAX_DELAY);
        if (status != HAL_OK)
        {
            DEBUG_PRINTF("QSPI command sending error in rdBuffer...\n");
            return status;
        }
        else
        {
			status = HAL_QSPI_Receive(&hqspi, DataToRead, HAL_MAX_DELAY);
			if (status != HAL_OK)
			{
				DEBUG_PRINTF("QSPI receive error in rdBuffer...\n");
				return status;
			}
        }
        MCU_bufferLen = 0;
    }

    return HAL_OK;
}

static int MCU_receive_buffer(uint8_t *DataToRead, uint32_t len, int end)
{
    HAL_StatusTypeDef status = HAL_OK;

    status = MCU_multi_transfer(DataToRead, len);
    if (status != HAL_OK)
	{
    	*DataToRead = 0;
    }

    return status;
}

static int MCU_append_buffer(const uint8_t *buffer, uint16_t length, int end)
{
    HAL_StatusTypeDef status = HAL_OK;
    int i = MCU_bufferLen;
    int j = 0;
    int plength;

    while (j < length)
    {
        plength = length - j;
        
        if (plength + MCU_bufferLen >= MCU_BUFFER_SIZE)
            plength = MCU_BUFFER_SIZE - MCU_bufferLen;
        
        /* NOTE: memcpy is used here as the libft4222 is not generally 
         * used for embedded systems.
         */
        memcpy(&MCU_buffer[i], &buffer[j], plength);
        j += plength;
        i += plength;
        MCU_bufferLen += plength;
        if (MCU_bufferLen >= MCU_BUFFER_SIZE)
        {
            status = MCU_multi_transfer(NULL, 0);

            if (status != HAL_OK)
			{
            	MCU_bufferLen = 0;
            	break;
            }
            /* Keep the previous write address in the transmit buffer if we are not
                * ending this transfer. */
            if (end == 0)
            {
                /* Refill buffer after address. */
#if IS_EVE_API(1, 2, 3, 4) /* Different write addressing method on BT82x */
                /* 24 bit write address followed by data */
                i = 3;
                MCU_bufferLen = 3;
#else
                /* 32 bit write address followed by data */
                i = 4;
                MCU_bufferLen = 4;
#endif
            }
        }
    }

    return status;
}

inline void MCU_CSlow(void)
{
    // CS# line low
    // Perform a very short delay
    __NOP();
}

inline void MCU_CShigh(void)
{
    // CS# line high
    MCU_multi_transfer(NULL, 0);
    // Perform a very short delay
    __NOP();
}

uint8_t MCU_SPIRead8(void)
{
    HAL_StatusTypeDef status;
    uint8_t DataRead = 0;

    status = MCU_receive_buffer((uint8_t *)&DataRead, 1, 0);
    if (HAL_OK != status)
    {
         // QUADSPI master read failed
        DEBUG_ERROR("FT4222 MCU_SPIRead8 failed %d\n", status);
        DataRead = 0;
    }
 
    return DataRead;
}

void MCU_SPIWrite8(uint8_t DataToWrite)
{
    MCU_append_buffer((uint8_t *)&DataToWrite, 1, 0);
}

